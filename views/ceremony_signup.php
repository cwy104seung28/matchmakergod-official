    $(document).on('click', '.number.amount .minus', function() {
        var $el = $(this).closest('.number.amount');
        var $numEl = $el.find('.inner');
        var count = parseInt($numEl.text());

        count = Math.max(0, count - 1);

        $numEl.text(count < 10 ? '0' + count : count);
    });

    // 增加
    $(document).on('click', '.number.amount .add', function() {
        var $el = $(this).closest('.number.amount');
        var $numEl = $el.find('.inner');
        var count = parseInt($numEl.text());

        count++;

        $numEl.text(count < 10 ? '0' + count : count);
    });

    function activityHandler(el) {
        let dragging = false;

        const $carousel = $(el).flickity({
            "prevNextButtons": false,
            "pageDots": false,
            "wrapAround": true,
            "autoPlay": false,
            "draggable": true,
            "cellAlign": "left",
            "imagesLoaded": true,
            "pauseAutoPlayOnHover": false,
            "arrowShape": "",
        })
        $("#prev-a").on("click", function() {
            $carousel.flickity('previous');
            $carousel.flickity('stopPlayer');
        })

        $("#next-a").on("click", function() {
            $carousel.flickity('next');
            $carousel.flickity('stopPlayer');
        })


        var cellElements = $carousel.flickity('getCellElements');


        $carousel.on('change.flickity', function(event, index) {
            $("#now-a").text(padLeft(index + 1, 2));
        });


        $("#total-a").text(padLeft(cellElements.length, 2));
    }
